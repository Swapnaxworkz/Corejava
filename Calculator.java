class Calculator
{
  static int add(int a)
  {
    return a;
  }
  static int add(int a, int b, int c, int d)  
  {
    return a+b+c;
  }
  static int sub(int a, int b)  
  {
    return a-b;
  }
  static int sub(int a, int b, int c)
  {
	  return a-b-c;
}
  static int mul(int a, int b)
  {
	  return a*b;
  }
  static int mul(int a, int b, int c)
  {
	  return a*b*c;
  }
}

